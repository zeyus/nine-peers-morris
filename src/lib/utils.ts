// Generate a random name
// following the pattern: adjective_1[separator]...adjective_(parts-1)[separator]noun
export const randomName = (parts: number = 3, separator: string = '-', capitalizeWords: boolean = false): string => {
    const adj = [];
    for (let i = 0; i < parts - 1; i++) {
        adj.push(adjectives[Math.floor(Math.random() * adjectives.length)]);
    }
    const name = [
        adj.join(separator),
        nouns[Math.floor(Math.random() * nouns.length)]
    ].join(separator);
    return capitalizeWords ? name.split(separator).map((s) => s.charAt(0).toUpperCase() + s.slice(1)).join(separator) : name;
}
